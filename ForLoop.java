public class ForLoop {
          public static void main(String[] args) {
                    for (int x = 1; x <= 5; x++) {
                              System.out.println("Bangladesh");
                    }
                    for (int y = 0; y <= 20; y += 2) {
                              System.out.println(y);
                    }
                    for (int z = 1; z <= 20; z += 2) {
                              System.out.println(z);
                    }
          }
}