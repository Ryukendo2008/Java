public class DoWhileLoop {
          public static void main(String[] args) {
                    int g = 5;
                    do {
                              System.out.println("I am a programmer");
                              g += 5;
                    } while (g <= 50);

                    int u = 0;
                    do {
                              // System.out.println(u);
                              u += 2;
                    } while (u <= 20);
          }
}
