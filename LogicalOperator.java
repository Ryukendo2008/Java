public class LogicalOperator {
          public static void main(String[] args) {
                    int Samsung = 15000;
                    int Walton = 90000;

                    if (Samsung > Walton && Samsung != Walton) {
                              System.out.println("I will buy Walton Phone.");
                    } else {
                              System.out.println("I will buy a Samsung Phone.");
                    }
          }
}
